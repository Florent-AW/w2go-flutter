/// Types de ripple disponibles pour différents cas d'usage
enum RippleType {
  /// Ripple compact - pour boutons d'action, icônes (scale: 1.0)
  compact,

  /// Ripple standard - pour boutons normaux (scale: 1.8)
  standard,

  /// Ripple étendu - pour cards, grandes surfaces (scale: 2.2)
  extended,
}